package arrayList_28_04;

public class Book {
	int id;
	int quantity;
	String name;
	String author;
	String publisher;
	
	
	public Book(int id, int quantity, String name, String author, String publisher) {
		this.id = id;
		this.quantity = quantity;
		this.name = name;
		this.author = author;
		this.publisher = publisher;
	}
	}
	

